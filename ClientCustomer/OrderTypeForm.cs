        public OrderTypeForm()
        {
            InitializeComponent();
            SetupEventHandlers();
            InitProvinceData();
        }

        private void InitProvinceData()
        {
            cmbProvince.Items.Clear();
            foreach (var province in RegionData.Keys)
            {
                cmbProvince.Items.Add(province);
            }
        }

        private void SetupEventHandlers()
        {
            panelDineIn.Click += (s, e) => SelectDineIn();

            foreach (Control control in panelDineIn.Controls)
            {
                if (control is not ComboBox)
                    control.Click += (s, e) => SelectDineIn();
            }

            panelDelivery.Click += (s, e) => SelectDelivery();

            foreach (Control control in panelDelivery.Controls)
            {
                if (control is not ComboBox)
                    control.Click += (s, e) => SelectDelivery();
            }

            btnConfirm.Click += BtnConfirm_Click;

            cmbProvince.SelectedIndexChanged += CmbProvince_SelectedIndexChanged;
            cmbCity.SelectedIndexChanged += CmbCity_SelectedIndexChanged;
        }

        private void CmbProvince_SelectedIndexChanged(object? sender, EventArgs e)
        {
            if (cmbProvince.SelectedItem == null) return;

            var province = cmbProvince.SelectedItem.ToString();
            cmbCity.Items.Clear();
            cmbDistrict.Items.Clear();
            cmbCity.Text = "";
            cmbDistrict.Text = "";

            if (RegionData.TryGetValue(province ?? "", out var cities))
            {
                foreach (var city in cities.Keys)
                {
                    cmbCity.Items.Add(city);
                }
            }

            if (cmbCity.Items.Count > 0)
                cmbCity.SelectedIndex = 0;
        }

        private void CmbCity_SelectedIndexChanged(object? sender, EventArgs e)
        {
            if (cmbProvince.SelectedItem == null || cmbCity.SelectedItem == null) return;

            var province = cmbProvince.SelectedItem.ToString();
            var city = cmbCity.SelectedItem.ToString();
            cmbDistrict.Items.Clear();
            cmbDistrict.Text = "";

            if (RegionData.TryGetValue(province ?? "", out var cities) &&
                cities.TryGetValue(city ?? "", out var districts))
            {
                foreach (var district in districts)
                {
                    cmbDistrict.Items.Add(district);
                }
            }

            if (cmbDistrict.Items.Count > 0)
                cmbDistrict.SelectedIndex = 0;
        }

        private void SelectDineIn()
        {
            OrderType = "DineIn";

            panelDineIn.BackColor = Color.FromArgb(255, 243, 224);
            panelDelivery.BackColor = Color.White;

            panelDineIn.BorderStyle = BorderStyle.FixedSingle;
            panelDelivery.BorderStyle = BorderStyle.None;

            txtTableNumber.Enabled = true;
            txtTableNumber.Focus();

            txtAddress.Enabled = false;
            lblAddress.Enabled = false;
            txtAddress.Clear();

            cmbProvince.Enabled = false;
            cmbCity.Enabled = false;
            cmbDistrict.Enabled = false;
            lblProvince.Enabled = false;
        }

        private void SelectDelivery()
        {
            OrderType = "Delivery";

            panelDelivery.BackColor = Color.FromArgb(227, 242, 253);
            panelDineIn.BackColor = Color.White;

            panelDelivery.BorderStyle = BorderStyle.FixedSingle;
            panelDineIn.BorderStyle = BorderStyle.None;

            txtAddress.Enabled = true;
            lblAddress.Enabled = true;
            txtAddress.Focus();

            cmbProvince.Enabled = true;
            cmbCity.Enabled = true;
            cmbDistrict.Enabled = true;
            lblProvince.Enabled = true;

            txtTableNumber.Enabled = false;
            txtTableNumber.Clear();
        }

        private void BtnConfirm_Click(object? sender, EventArgs e)
        {
            if (OrderType == "DineIn")
            {
                if (string.IsNullOrWhiteSpace(txtTableNumber.Text))
                {
                    MessageBox.Show("请输入桌号！", "提示",
                        MessageBoxButtons.OK, MessageBoxIcon.Warning);
                    txtTableNumber.Focus();
                    return;
                }
                TableNumber = txtTableNumber.Text.Trim();
            }
            else
            {
                if (cmbProvince.SelectedItem == null)
                {
                    MessageBox.Show("请选择省份！", "提示",
                        MessageBoxButtons.OK, MessageBoxIcon.Warning);
                    cmbProvince.Focus();
                    return;
                }

                if (string.IsNullOrWhiteSpace(txtAddress.Text))
                {
                    MessageBox.Show("请输入详细地址！", "提示",
                        MessageBoxButtons.OK, MessageBoxIcon.Warning);
                    txtAddress.Focus();
                    return;
                }

                var province = cmbProvince.SelectedItem?.ToString() ?? "";
                var city = cmbCity.SelectedItem?.ToString() ?? "";
                var district = cmbDistrict.SelectedItem?.ToString() ?? "";

                var regionParts = new[] { province, city, district };
                var region = string.Join("", regionParts.Where(p => !string.IsNullOrEmpty(p)));
                var detail = txtAddress.Text.Trim();

                DeliveryAddress = string.IsNullOrEmpty(region) ? detail : $"{region}{detail}";
            }

            this.DialogResult = DialogResult.OK;
            this.Close();
        }
